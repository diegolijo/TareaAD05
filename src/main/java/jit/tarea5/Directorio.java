package jit.tarea5;

public class Directorio {

    private int id;
    private String nombre;

    public Directorio(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public Directorio() {
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }
    
    

}
