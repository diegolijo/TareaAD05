package Objetos;

public class Directorio {

    private int id;
    private String nombre;
    
    public Directorio() {
    }


    public Directorio(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

 
    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }
    
    

}
